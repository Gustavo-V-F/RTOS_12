/* USER CODE BEGIN Header_vSender_task */
/**
  * @brief  Function implementing the Sender1 and Sender2 threads.
  * @param  argument: lValue_to_send 
  * @retval None
  */
/* USER CODE END Header_vSender_task */
void vSender_task(void const * argument)
{
  /* USER CODE BEGIN 6 */
  osStatus xQueue_status;
  /* Infinite loop */
  for(;;)
  {
    xQueue_status = osMessagePut(Queue_handle, (uint32_t) argument, 100);
    
    if(xQueue_status != osOK)
    {
      printf("\r\nCould not send the queue.");
      HAL_Delay(1);
    }
    
    osThreadYield();
  }
  /* USER CODE END 6 */ 
}

/* USER CODE BEGIN Header_vReceiver_task */
/**
  * @brief  Function implementing the Receiver thread.
  * @param  argument: None
  * @retval None
  */
/* USER CODE END Header_vReceiver_task */
void vReceiver_task(void const * argument)
{
  /* USER CODE BEGIN 7 */
  osEvent eValue_to_receive;
  xData *xReceived_structure;
  /* Infinite loop */
  for(;;)
  {
    if(osMessageWaiting(Queue_handle) != 3)
    {
      printf("\r\nQueue should have been full!");
      HAL_Delay(1);
    }
      
    eValue_to_receive = osMessageGet(Queue_handle, 0);

    if(eValue_to_receive.status == osEventMessage)
    {
      xReceived_structure = (xData *) eValue_to_receive.value.v;
      if(xReceived_structure->ucSource == 1)
        printf("\r\nFrom Sender 1 = %u", xReceived_structure->ucValue);
      else
        printf("\r\nFrom Sender 2 = %u", xReceived_structure->ucValue);
    }
    else
      printf("\r\nCould not receive from the queue.");

    HAL_Delay(1);
  }
  /* USER CODE END 7 */ 
}

